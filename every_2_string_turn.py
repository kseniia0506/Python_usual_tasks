import random

a = []
for i in range(100):
    a.append(round(random.random() * 50))

i = 1  # обычный вывод для сравнения
while i <= 100:
    print('%4d' % a[i - 1], end='')
    if i % 10 == 0: print()
    i += 1

print()
i = 0  # вывод по спирали
while i < 10:
    if i % 2 == 0:
        j = i * 10
        k = j + 9
        while j <= k:
            print('%4d' % a[j], end='')
            j += 1
        print()
    else:
        j = i * 10
        k = j + 9
        while j <= k:
            print('%4d' % a[k], end='')
            k -= 1
        print()
    i += 1

