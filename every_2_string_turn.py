# Зная номер строки и то, что элементов в строке 10, можно вычислить индексы первого и последнего элементов в строке.
# Далее в цикле вывести элементы либо от первого к последнему, либо от последнего к первому в зависимости
# от четности строки.

# Номер первого элемента в строке можно определить по формуле i * 10, где i - номер строки.
# Действительно, если строка нулевая, то индекс первого элемента будет 0, а если строка первая, то индекс будет 10.
# Индекс же последнего элемента в строке отличается от первого на 9 единиц, хотя всего элементов в строке 10.

